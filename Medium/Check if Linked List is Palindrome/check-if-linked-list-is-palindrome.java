class Solution
{
    //Function to check whether the list is palindrome.
    boolean isPalindrome(Node head) 
    {
        //Your code here
        if (head == null || head.next == null) return true;
        
        Node s = head;
        Node f = head;
        
        //First of all we will find the mid
        while(f.next!= null && f.next.next != null){
            f = f.next.next;
            s = s.next;
        }
        
        //After that we will reverse the LL from the mid
        s = reverse(s.next);
        
        //Now we will run the loop till slow gets null and only if
        // both data's are equal then we will enter the loop or we will not
        while(s!= null && (s.data == head.data)){
            head = head.next;
            s = s.next;
        }
        
        return s == null;
    } 
    
    Node reverse(Node head){
        Node prev = null;
        Node curr = head;
        Node forw = null;
        
        while(curr != null){
            forw = curr.next;
            curr.next = prev;
            prev = curr;
            curr = forw;
        }
        return prev;
    }
}

